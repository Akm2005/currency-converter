import React, { useState } from 'react';
import './Home.css';



const Home = () => {
  const [from, setFrom] = useState('INR');
  const [to, setTo] = useState('USD');
  const [input, setInput] = useState('');
  const [result, setResult] = useState('');

  const fetchExchangeRate = async () => {
    try {
      const response = await fetch(`https://cdn.jsdelivr.net/gh/fawazahmed0/currency-api@1/latest/currencies/${from.toLowerCase()}/${to.toLowerCase()}.json`);
      const data = await response.json();
      setResult(data[to.toLowerCase()]);
      console.log(data[to.toLowerCase()]);
    } catch (error) {
      console.error('Error fetching exchange rate:', error);
      // Handle errors, e.g., set an error state or show a message to the user
    }
  };

  const handleExchange = () => {
    fetchExchangeRate();
  };

  return (
    <div className='man'>
      <div className='container'>
        <form className='main'>
          <i><h2 className='h'>Currency Converter</h2></i>
          <input
            type='number'
            className='inputs'
            placeholder='Enter amount Here'
            value={input}
            onChange={(e) => {
              setInput(e.target.value);
              setResult('');
            }}
           
          />
          <div className='exchange'>
            <select className='from' value={from} onChange={(e) => {setFrom(e.target.value);setResult('');}}>
              {Object.keys(countryList).map(currency => (
                <option key={currency} value={currency}>{currency}</option>
              ))}
            </select>
            ðŸ’±
            <select className='to' value={to} onChange={(e) => {setTo(e.target.value);setResult('');}}>
              {Object.keys(countryList).map(currency => (
                <option key={currency} value={currency}>{currency}</option>
              ))}
            </select>
          </div>

          <p className='p'>{input} {from} = {input * result} {to}</p>
          <button type='button' className='btn1' onClick={handleExchange}>
            Exchange
          </button>
        </form>
      </div>
    </div>
  );
};
